export function getWelcomeMessage(index?: number): string {
  if (index !== undefined) {
    return welcomeMessages[index % welcomeMessages.length]
  }
  // Use day of year for daily rotation
  const now = new Date()
  const start = new Date(now.getFullYear(), 0, 0)
  const diff = now.getTime() - start.getTime()
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24))
  return welcomeMessages[dayOfYear % welcomeMessages.length]
}

export function getRandomAchievement(): string {
  // Anti-repeat: use timestamp-based selection
  const seed = Date.now() % achievementMessages.length
  return achievementMessages[seed]
}

export function getDailyMessage(): string {
  const now = new Date()
  const dayOfYear = Math.floor((now.getTime() - new Date(now.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24))
  // Use a different offset from welcome messages
  const offset = (dayOfYear * 7 + 13) % welcomeMessages.length
  return welcomeMessages[offset]
}
